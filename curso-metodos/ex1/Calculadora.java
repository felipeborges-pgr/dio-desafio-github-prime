package com.dio.ex1;

public class Calculadora {


	public static void soma(double n1, double n2) {

		double resultado;

		resultado = n1 + n2;

		System.out.println("O resultado da soma é igual a: " + resultado);
		
	}
	
	public static void subtracao(double n1, double n2) {

		double resultado;

		resultado = n1 - n2;

		System.out.println("O resultado da subtração é igual a: " + resultado);
		
	}

	public static void multiplicacao(double n1, double n2) {

		double resultado;

		resultado = n1 * n2;

		System.out.println("O resultado da multiplicação é igual a: " + resultado);
		
	}

	public static void divisao(double n1, double n2) {

		double resultado;

		resultado = n1 / n2;

		System.out.println("O resultado da divisão é igual a: " + resultado);
		
	}
	
	
}